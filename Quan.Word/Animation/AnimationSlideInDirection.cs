namespace Quan
{
    /// <summary>
    /// The direction an animation slides in (the slide out direction is reversed)
    /// </summary>
    public enum AnimationSlideInDirection
    {
        /// <summary>
        /// Slide in from the left
        /// </summary>
        Left = 0,
        /// <summary>
        /// Slide in from the right
        /// </summary>
        Right = 1,
        /// <summary>
        /// Slide in from the top
        /// </summary>
        Top = 2,
        /// <summary>
        /// Slide in from the bottom
        /// </summary>
        Bottom = 3
    }
}
